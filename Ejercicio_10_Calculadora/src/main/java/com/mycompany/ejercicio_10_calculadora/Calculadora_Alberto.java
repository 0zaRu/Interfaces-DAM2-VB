    private void actionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionPerformed
        //CODIGO PARA CUANDO SE PULSA UNA TECLA DE LA APLICACION
        JButton pulsado = (JButton)evt.getSource();
        tPantalla.requestFocus();
        
        if(pulsado.getText().equals("")){
            if(!tPantalla.getText().isEmpty())
                tPantalla.setText(tPantalla.getText().substring(0, tPantalla.getText().length()-1));
        
        }else if(pulsado.getText().length() == 3){
            //Boton de cambio de signo
            if(tPantalla.getText().charAt(0) == '-'){
                tPantalla.setText(tPantalla.getText().replace("-", ""));
            }else if(!tPantalla.getText().isEmpty())
                tPantalla.setText("-"+tPantalla.getText());
            
        }else if(pulsado.getText().length() == 2){
            //Boton de reinicio texto actual
            tPantalla.setText("");
        
        }else{
            //Botones con texto, signos o numeros
            if(Character.isDigit(pulsado.getText().charAt(0)) && tPantalla.getText().length() <= 14){
                tPantalla.setText(tPantalla.getText()+pulsado.getText());
            
            }else if(!pulsado.getText().equals("=") && !pulsado.getText().equals("C") && !pulsado.getText().equals(".") && tPrevio.getText().isEmpty()){
                if(!tPantalla.getText().isEmpty()){
                    tPrevio.setText(tPantalla.getText()+" "+pulsado.getText()+" ");
                    tPantalla.setText("");
                }
            
            }else if(pulsado.getText().equals("C")){
                tPrevio.setText("");
                tPantalla.setText("");
            
            }else if(pulsado.getText().equals(".")){
                tPantalla.setText(tPantalla.getText()+".");
            
            }else if(!tPantalla.getText().isEmpty()){
                //Tratamos operaciones, cuando sea igual o cuando sea un simbolo con otro ya pulsado antes
                trataPosiblesOperadores(pulsado);
            }
        }
    }//GEN-LAST:event_actionPerformed

    public void trataPosiblesOperadores(JButton pulsado){
        if(pulsado.getText().equals("=") && tPrevio.getText().isEmpty())
            tPrevio.setText(tPantalla.getText()+" =");
        
        else if(pulsado.getText().equals("=") && !tPrevio.getText().isEmpty()){
            tPrevio.setText(tPrevio.getText()+tPantalla.getText()+" = ");
            tPantalla.setText(""+operaConTextoPrevio());
        
        }else if(!pulsado.getText().equals("=") && !tPrevio.getText().isEmpty()){
            tPrevio.setText(tPrevio.getText()+tPantalla.getText()+" = ");
            tPrevio.setText(""+operaConTextoPrevio()+" "+pulsado.getText()+" ");
        }
    }
    
    public double operaConTextoPrevio(){
        // Eliminar espacios en blanco y dividir la cadena en partes
        String operacion = tPrevio.getText();
        String[] partes = operacion.replaceAll("\\s", "").split("[\\+\\-X/=]");

        // Obtener los operandos y el operador
        double operando1 = Double.parseDouble(partes[0]);
        double operando2 = Double.parseDouble(partes[1]);
        char operador = operacion.replaceAll("[^\\+\\-X/=]", "").charAt(0);

        // Verificar el signo del primer operando
        if (operacion.startsWith("-")) {
            operando1 = -operando1;
        }

        // Realizar la operación
        switch (operador) {
            case '+':
                return operando1 + operando2;
            case '-':
                return operando1 - operando2;
            case 'X':
                return operando1 * operando2;
            case '/':
                if (operando2 != 0) {
                    return operando1 / operando2;
                } else {
                    throw new ArithmeticException("División por cero no permitida");
                }
            default:
                throw new IllegalArgumentException("Operador no válido: " + operador);
        }
    }
    
