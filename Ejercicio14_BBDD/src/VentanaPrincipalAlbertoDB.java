    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Deseas salir del programa?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) {
            System.out.println("¡Hasta luego!");
            
            ConexionBD.cerrarConexion(cc.getConexion());
            System.exit(0);
        }
    }//GEN-LAST:event_jMenu2MouseClicked

    private void jMenu3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu3MouseClicked
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Deseas reiniciar la base de datos?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) {
            cc.reestablecerBaseDeDatos();
            cargaInicial();
        }
    }//GEN-LAST:event_jMenu3MouseClicked

    void cargaInicial(){
        jPanel2.setVisible(false);
        jPanel3.setVisible(false);
        txt1.setVisible(false);
        txt2.setVisible(false);
        
        cc.cargarLibrosEnTabla(jtTablaLibros);
        cc.cargarAutoresEnTabla(jtTablaAutores);
        
        libroElegidoId = -1;
        autorElegidoId = -1;
    }
    
