    public VentanaPrincipalAlbertoDB() {
        initComponents();
        
        Connection conexion = ConexionBD.obtenerInstancia().obtenerConexion();
        
        if(conexion == null){
            JOptionPane.showMessageDialog(null, "Problema al conectar con la base da datos. No existe o el config.properties está mal configurado");
            System.exit(-1);
        }
        
        cc = new ConexionController(conexion);
        cargaInicial();
        
        jtTablaLibros.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting()) {
                filaElegidaLibros = jtTablaLibros.getSelectedRow();
                if (filaElegidaLibros != -1) {
                    libroElegidoId = (int) jtTablaLibros.getValueAt(filaElegidaLibros, 0);
                    
                    
                    jPanel2.setVisible(true);
                    jPanel3.setVisible(true);
                    txt1.setVisible(true);
                    txt2.setVisible(true);
                    
                    String[] datos = cc.obtenerDatosLibroYAutor(libroElegidoId);
                    
                    idLibro.setText("Id: "+datos[0]);
                    titulo.setText("Título: "+datos[1]);
                    autor.setText("Autor: "+datos[4]);
                    fechaPubli.setText("Publicado en: "+datos[2]);
                    idAutor.setText("Id: "+datos[3]);
                    nombre.setText("Nombre: "+datos[4]);
                    nacionalidad.setText("Nacionalidad: "+datos[5]);
                    fechaNaci.setText("Fecha de nacimiento: "+datos[6]);
                }
            }
        });
        
        jtTablaAutores.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting()) {
                filaElegidaAutores = jtTablaAutores.getSelectedRow();
                if (filaElegidaAutores != -1) {
                    autorElegidoId = (int) jtTablaAutores.getValueAt(filaElegidaAutores, 0);
                }
            }
        });
        
        
    }

