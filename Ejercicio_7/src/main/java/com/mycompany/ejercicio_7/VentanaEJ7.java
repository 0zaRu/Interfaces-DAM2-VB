    private void borrarFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarFilaActionPerformed
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        
        int a = tabla.getSelectedRow();
        
        if(a<0){
            JOptionPane.showMessageDialog(null, "Debes selecionar una fila de la tabla");
        
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "¿Está seguro de borrar la fila?");
            if(JOptionPane.OK_OPTION == confirmar){
                modelo.removeRow(a);
                JOptionPane.showMessageDialog(null, "Fila borrada correctamente");
            }
        }
    }//GEN-LAST:event_borrarFilaActionPerformed

