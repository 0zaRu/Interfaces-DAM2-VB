    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(!compruebaCampoVacio(codigo, "Código") ||
           !compruebaCampoVacio(descripcion, "Descripción") ||
           !compruebaCampoVacio(numeroHoras, "Número de horas") ||
           !compruebaCampoVacio(nombre, "Nombre") ||
           !compruebaCampoVacio(precioHoraTrabajo, "Precio de hora de trabajo"))
            return;
        
        Empleado nuevoEmpleado = new Empleado(nombre.getText(), (Double)precioHoraTrabajo.getValue(), codigo.getText(), descripcion.getText(), (Double)numeroHoras.getValue());
        
        Object[] addTabla = new Object[6];
        addTabla[0] = nuevoEmpleado.getCodigo();
        addTabla[1] = nuevoEmpleado.getDescripcion();
        addTabla[2] = nuevoEmpleado.getNumeroHoras();
        addTabla[3] = nuevoEmpleado.getNombre();
        addTabla[4] = nuevoEmpleado.getPrecioHoraTrabajo();
        addTabla[5] = nuevoEmpleado.getSueldoFinal();
        
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        modelo.addRow(addTabla);
        
        codigo.setText("");
        descripcion.setText("");
        nombre.setText("");
        numeroHoras.setValue(0);
        precioHoraTrabajo.setValue(0);
    }//GEN-LAST:event_addActionPerformed

